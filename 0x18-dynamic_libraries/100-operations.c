#include <stdio.h>


/**
 * add - add two numbers
 * @a: member
 * @b: member
 * Return: sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: member
 * @b: member
 * Return: sum of a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: member
 * @b: member
 * Return: sum of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: member
 * @b: member
 * Return: sum of a and b
 */

int div(int a, int b)
{
	return (a / b);
}



/**
 * mod - mod division two numbers
 * @a: member
 * @b: member
 * Return: sum of a and b
 */

int mod(int a, int b)
{
	return (a % b);
}


